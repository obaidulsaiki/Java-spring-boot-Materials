public class DataTypes {
    DataTypes() {
        // in java there are 2 types of datatype
        //1. primitives - byte, int, long ,float, double , boolean and char
        //byte - stores whole number from -128 to 127
        byte Byte_number = 20;
        System.out.println("Byte number : " + Byte_number);
        //int - stores whole number from -2^32 to 2^31 bit (4 bytes)
        int Int_number = 2420;
        System.out.println("Integer number: "+ Int_number);
        //long - stores more big number from -2^64 to 2^63 bit (8 bytes)
        long Long_number = 2420;
        System.out.println("long number: "+ Long_number);
        //float - stores fractional value (6 to 7 decimal digits)
        float Float_number = 0.966f;
        System.out.println("Float number" + Float_number);
        //double - stores fractional number upto 15 to 16 digits
        double Double_number = 1200000.232323;
        System.out.println("Double number: " + Double_number);
        //Boolean - store true or false value
        boolean Bool_Data = true;
        System.out.println("Boolean Number: "+ Bool_Data);
        //char - stores one character 1,2,A,# etc
        char char_data = '@';
        System.out.println("Character : "+ char_data);
        //string - sequential characters
        String stringChar = "ThisIsAString";
        System.out.println("String: "+ stringChar);

    }
}
